package com.model;

public class SSN {

	private String type;
	private String no;
	public SSN(String type, String no) {
		super();
		this.type = type;
		this.no = no;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	
	

	
	
}
